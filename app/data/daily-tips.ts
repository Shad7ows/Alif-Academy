/**
 * الحصول على نصيحة اليوم بناءً على التاريخ
 * تتغير النصيحة كل يوم
 */
export function getDailyTip(): string {
  const today = new Date();
  // استخدام الوقت بالمللي ثانية مقسوماً على يوم واحد للحصول على رقم اليوم
  const dayIndex = Math.floor(today.getTime() / (1000 * 60 * 60 * 24));
  // استخدام باقي القسمة لضمان التكرار
  return DAILY_TIPS[dayIndex % DAILY_TIPS.length];
}